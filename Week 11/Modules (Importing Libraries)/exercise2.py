# Use 5 Functions in Python Math Module  and print the results 
import math

number = int(9)
print(f"Original number: {number}")

print(f"Square root: {math.sqrt(number)}")
print(f"Factorial: {math.factorial(number)}")
print(f"Remainder when divided by 4: {math.remainder(number,4)}")
print(f"Raised to the power of 3: {math.pow(number, 3)}")
print(f"GCD with 24: {math.gcd(number, 24)}")